CREATE TABLE IF NOT EXISTS `DemoConfig`.`Organisation` (
  `ID` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `Name` varchar(50) NOT NULL,
  `Address` text NOT NULL,
  `Active` enum('YES','NO') NOT NULL,

 PRIMARY KEY (`ID`),
 UNIQUE KEY `Name_Address` (`Name`, `Address`(100)),
 KEY `Active` (`Active`)
);

--
-- Table structure for table `User`
--

CREATE TABLE IF NOT EXISTS `DemoConfig`.`User` (
  `ID` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `OrganisationID` int(10) unsigned NOT NULL,
  `Name` varchar(30) NOT NULL,
  `Email` varchar(50) NOT NULL,
  `Address` text NOT NULL,
  `Active` enum('YES','NO') NOT NULL,

 PRIMARY KEY (`ID`),
 KEY `Email` (`Email`,`Active`)
);

ALTER TABLE `User`
  ADD FOREIGN KEY (`OrganisationID`) REFERENCES `DemoConfig`.`Organisation`(`ID`)
    ON DELETE CASCADE ON UPDATE CASCADE;

--
-- Table structure for table `UserPassword`
--

CREATE TABLE IF NOT EXISTS `DemoConfig`.`UserPassword` (
  `ID` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `UserID` int(10) unsigned NOT NULL,
  `Hash` binary(40) NOT NULL,
  `CreatedAt` datetime NOT NULL,
  `ExpiresAt` datetime NOT NULL,

 PRIMARY KEY (`ID`),
 KEY `UserID_Hash` (`UserID`,`Hash`),
 KEY `CreatedAt` (`CreatedAt`),
 KEY `ExpiresAt` (`ExpiresAt`),

 CONSTRAINT `UserPassword_User_ID_fk` FOREIGN KEY (`UserID`) REFERENCES `DemoConfig`.`User` (`ID`) ON DELETE CASCADE
);

